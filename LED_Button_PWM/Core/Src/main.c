void zaswiec_dioda(int channel, int level) {
    switch (channel) {
        case 1:
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, level); // zaswiec dioda (kanal 1) za pomoca PWM
            break;
        case 2:
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, level); // zaswiec dioda (kanal 2) za pomoca PWM
            break;
        default:
            break;
    }
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) // funkcja obslugujaca przerwania
{
    if (htim == &htim6) // jestli przerwanie pochodzi z timera 6 to:
    {
        // Sprawdź stan przycisków i zmień stan odpowiednich LEDów
        if (HAL_GPIO_ReadPin(BUTTON1_GPIO_Port, BUTTON1_Pin) == GPIO_PIN_RESET)
        {
            // Przycisk 1 nie jest naciśnięty
        	zaswiec_dioda(1, 0);
        }
        else
        {
            // Przycisk 1 jest naciśnięty
        	zaswiec_dioda(1, 1000); 		// wypelnienie diody na poziomie 1000
        }

        if (HAL_GPIO_ReadPin(BUTTON2_GPIO_Port, BUTTON2_Pin) == GPIO_PIN_RESET)
        {
            // Przycisk 2 nie jest naciśnięty
        	zaswiec_dioda(2, 0);
        }
        else
        {
            // Przycisk 2 jest naciśnięty
        	zaswiec_dioda(2, 50);			// wypelnienie diody na poziomie 50
        }
    }
}



